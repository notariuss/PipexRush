#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	c1;
	char	*str;

	str = (char*)s;
	c1 = (char)c;
	while (*str != c1)
	{
		if (*str == '\0')
			return (0);
		str++;
	}
	return (str);
}
